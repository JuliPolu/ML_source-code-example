from typing import List


def sum_of_two_numbers_list(numbers: List[float]) -> float:
    """
    This function returns the sum of a list of numbers.

    Parameters:
        numbers: List[float] - A list of numbers to sum.

    Returns:
        float - The sum of the numbers.
    """

    return sum(numbers)
